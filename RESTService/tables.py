from flask_table import Table, Col, LinkCol


class Results(Table):
    user_id = Col('Id', show=False)
    user_name = Col('Name')
    user_login = Col('Login')
    user_onboarded = Col('Onboarded')
    user_dept = Col('Department')
    delete = LinkCol('Delete', 'delete_user', url_kwargs=dict(id='user_id'))

